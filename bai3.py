# Escape Sequence.
# \a : phát ra tiếng bíp
# \b : lùi con trỏ về và xóa kí tự trước đó (các \b ở đầu và cuối chuỗi bị bỏ qua)
# \n : đưa con trỏ xuống dòng tiếp theo
# \t : in một horizonal tab (giống ta tab khi gõ văn bản, tab này có độ dài là 8)
# \' : in ra dấu '
# \" : in ra dấu "
# \\ : in ra dấu \

