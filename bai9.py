# Phương thức count
# Cú pháp: <List>.count(sub, [start, [end]]) : Giống với phương thức count của kiểu dữ liệu chuỗi.

# Phương thức index
# Cú pháp: <List>.index(sub[, start[, end]]) : Tương tự phương thức index của  kiểu dữ liệu chuỗi.

# Phương thức copy
# Cú pháp: <List>.copy() :  Trả về một List tương tự với List[:]

# Phương thức clear
# Cú pháp: <List>.clear() : Xóa mọi phần tử có trong List.

'''
Các phương thức cập nhật
'''

# Phương thức append
# Cú pháp: <List>.append(x) :  Thêm phần tử x vào cuối List

# Phương thức extend
# Cú pháp: <List>.extend(iterable) : Thêm từng phần tử một của iterable vào cuối List.

# Phương thức insert
# Cú pháp: <List>.insert (i, x) : Thêm phần x vào vị trí i ở trong List.

# Phương thức pop
# Cú pháp: <List>.pop([i]) : Bỏ đi phần tử thứ i trong List và trả về giá trị đó. Nếu vị trí i không được cung cấp, 
# phương thức này sẽ tự bỏ đi phần tử cuối cùng của List và trả về giá trị đó.

# Phương thức remove
# Cú pháp: <List>.remove(x) : Bỏ đi phần tử đầu tiên trong List có giá trị x. 
# Nếu trong List không có giá trị x sẽ có lỗi được thông báo

